# Basic Python examples

# 1. Print a simple message
print("Hello, World!")

# 2. Variables and string formatting
name = "Python Learner"
age = 25
print(f"Hello {name}, you are {age} years old!")

# 3. Basic math operations
x = 10
y = 5
print(f"\nMath Operations:")
print(f"{x} + {y} = {x + y}")
print(f"{x} - {y} = {x - y}")
print(f"{x} * {y} = {x * y}")
print(f"{x} / {y} = {x / y}")

# 4. Basic list operations
fruits = ["apple", "banana", "orange"]
print(f"\nMy fruit list: {fruits}")
print(f"First fruit: {fruits[0]}")
print(f"Last fruit: {fruits[-1]}")

# 5. Simple input example
print("\nLet's interact!")
user_input = input("What's your name? ")
print(f"Nice to meet you, {user_input}!") 